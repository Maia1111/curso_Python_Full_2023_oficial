# Operadores relacionais
# São operadores de comparação entre dados.

# Quais são os operadores relacionais:
# > maior que
# < menor que
# >= maior ou igual
# <= menor ou igual
# == igual 
# != diferente

# Exemplo de uso de operadores relacionais
# O operador relacional só retorna True ou False


operador_relacional = 2 > 1
print(operador_relacional)

operador_relacional = 2 < 1
print(operador_relacional)

operador_relacional = 2 >= 1
print(operador_relacional)

operador_relacional = 2 <= 1
print(operador_relacional)

operador_relacional = 2 == 1
print(operador_relacional)

operador_relacional = 2 != 1
print(operador_relacional)

# Exemplo de uso de operadores relacionais com variáveis

idade = 18
idade2 = 21

operador_relacional = idade > idade2
print(operador_relacional)