# Estrutura de decisão
# A estrutura de decisão é utilizada para decidir se um bloco de instruções será executado ou não, dependendo de uma condição.

# Sintaxe da estrutura de decisão:
# if condição:
#     bloco de instruções

# Exemplo de uso da estrutura de decisão:
# elif condição:
#     bloco de instruções


if False:
    print('False')

elif True:
    print('True')

else:
    print('Else')



