# Operadores lógicos
# São operadores que trabalham com valores booleanos, ou seja, True ou False.

# Quais são os operadores lógicos:
# and - e
# or - ou
# not - não

# Operador and
# O operador and retorna True se as duas condições forem verdadeiras.

operador = 5 == 5 and 3 > 4
print(operador)

operador = 5 == 5 and 3 < 4
print(operador)

# Operador or
# O operador or retorna True se uma das condições for verdadeira.
operador = 5 == 6 or 4 != 5
print(operador)

operador = 5 == 6 or 4 == 5
print(operador)

# Operador not
# O operador not inverte o valor booleano.
operador = not 5 == 6
print(operador)

operador = not 5 == 5
print(operador)

